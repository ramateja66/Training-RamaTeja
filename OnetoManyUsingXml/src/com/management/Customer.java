package com.management;

public class Customer 
{
	private int cid;
	private String cname;
	private int fvid;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getFvid() {
		return fvid;
	}
	public void setFvid(int fvid) {
		this.fvid = fvid;
	}
	

}
